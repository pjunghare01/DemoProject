public class SampleIFCondition {

	public static void main(String[] args) {
		int age = 250;  // this is input
		if(age>=18) {     // statisfying the condition :- if condition(true).
			System.out.println("The person is eligible for voting");
		}
		else {    // if false
			System.out.println("The person is not eligiable for voting");
		}
	}

}
